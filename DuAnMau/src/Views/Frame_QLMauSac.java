    private QLMauSac validateThongTin() {

        boolean isValid = true;
        String ma = txtMaMauSac.getText();
        if (ma.isBlank()) {
            JOptionPane.showMessageDialog(this, "Khong duoc bo trong ma");
            isValid = false;
        }

        String ten = txtTenMauSac.getText();
        if (ten.isBlank()) {
            JOptionPane.showMessageDialog(this, "Khong duoc bo trong mau");
            isValid = false;
        }

        QLMauSac ms = null;
        if (isValid == true) {
            ms = new QLMauSac(ma, ten);
        }
        return ms;
    }

    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblMauSac.getModel();
        model.setRowCount(0);
        List<QLMauSac> listMauSac = iManageMauSacService.getAll();
        for (QLMauSac mauSac : listMauSac) {
            model.addRow(new Object[]{
                mauSac.getId(), mauSac.getMaMau(), mauSac.getTenMau()
            });
        }
    }

    private void loadTable2(List<QLMauSac> list) {
        DefaultTableModel model = (DefaultTableModel) tblMauSac.getModel();
        model.setRowCount(0);
        for (QLMauSac mauSac : list) {
            model.addRow(new Object[]{
                mauSac.getId(), mauSac.getMaMau(), mauSac.getTenMau()
            });
        }
    }

