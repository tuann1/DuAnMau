    private void searchOnKeyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchOnKeyKeyReleased
        String search = searchOnKey.getText();
        try {
            List<QLMauSac> a = iManageMauSacService.getMauSacByMa(search);
            loadTable2(a);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_searchOnKeyKeyReleased

    private String getMauSacById() {
        int rowSelected = tblMauSac.getSelectedRow();
        return (String) tblMauSac.getValueAt(rowSelected, 0);
    }

