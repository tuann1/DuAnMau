    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);
        List<QLSanPham> list = imanageSanPham.getAll();
        for (QLSanPham qLSp : list) {
            model.addRow(new Object[]{
                qLSp.getIdSp(), qLSp.getMaSp(), qLSp.getTenSp()
            });
        }
    }

    private void loadTable2(List<QLSanPham> list) {
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);
        for (QLSanPham qLSp : list) {
            model.addRow(new Object[]{
                qLSp.getIdSp(), qLSp.getMaSp(), qLSp.getTenSp()
            });
        }
    }

    private QLSanPham validateThongTin() {

        boolean isValid = true;
        String maSp = txtMaSP.getText();
        if (maSp.isBlank()) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống mã sản phẩm");
            isValid = false;
        }

        String tenSp = txtTenSP.getText();
        if (tenSp.isBlank()) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống tên sản phẩm");
            isValid = false;
        }

        QLSanPham sp = null;
        if (isValid == true) {
            sp = new QLSanPham(maSp, tenSp);
        }

        return sp;
    }

