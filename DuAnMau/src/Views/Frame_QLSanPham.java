    private void reset() {
        txtMaSP.setText("");
        txtTenSP.setText("");
    }

    private void btnThemSpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSpActionPerformed
        // TODO add your handling code here:
        QLSanPham qlSp = validateThongTin();
        if (qlSp == null) {
            JOptionPane.showMessageDialog(this, "Thất bại");
            return;
        }

        if (imanageSanPham.save(qlSp) != null) {
            JOptionPane.showMessageDialog(this, "Thành công");
            reset();
        } else {
            JOptionPane.showMessageDialog(this, "Thất bại");
        }
        loadTable();
    }//GEN-LAST:event_btnThemSpActionPerformed

    private void btnXoaSpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaSpActionPerformed
        // TODO add your handling code here:
        int rowSelected = tblSanPham.getSelectedRow();
        if (rowSelected != -1) {
            String idSp = tblSanPham.getValueAt(rowSelected, 0).toString();
            imanageSanPham.delete(idSp);
            loadTable();
            JOptionPane.showMessageDialog(this, "Thành công");

            reset();
        } else {
            JOptionPane.showMessageDialog(this, "Chưa có thông tin SP cần xóa!");
        }
    }//GEN-LAST:event_btnXoaSpActionPerformed

    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        // TODO add your handling code here:
        int rowSelected = tblSanPham.getSelectedRow();
        if (rowSelected != -1) {
            String maSp = (String) tblSanPham.getValueAt(rowSelected, 1);
            String tenSp = (String) tblSanPham.getValueAt(rowSelected, 2);

            txtMaSP.setText(maSp);
            txtTenSP.setText(tenSp);
        }
    }//GEN-LAST:event_tblSanPhamMouseClicked

    private String getSelectedId() {
        int rowSelected = tblSanPham.getSelectedRow();
        return (String) tblSanPham.getValueAt(rowSelected, 0);
    }

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        QLSanPham updateSanPham = validateThongTin();
        if (updateSanPham == null) {
            JOptionPane.showMessageDialog(this, "Khong co data");
            return;
        }

        String id = getSelectedId();
        updateSanPham.setIdSp(id);

        if (imanageSanPham.updateSanPham(updateSanPham) != null) {
            JOptionPane.showMessageDialog(this, "Thanh cong");
            reset();
        } else {
            JOptionPane.showMessageDialog(this, "That bai");
        }
        loadTable();
    }//GEN-LAST:event_btnSuaActionPerformed

