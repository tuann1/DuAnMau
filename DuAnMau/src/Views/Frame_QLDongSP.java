    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int rowSelected = tblDongSp.getSelectedRow();
        if (rowSelected != -1) {
            String id = (String) tblDongSp.getValueAt(rowSelected, 0);
            imangeDongSp.delete(id);
            JOptionPane.showMessageDialog(this, "Thành công!");
            loadTable();
            reset();
        } else {
            JOptionPane.showMessageDialog(this, "Thất bại");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblDongSpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDongSpMouseClicked
        // TODO add your handling code here:
        int rowSelected = tblDongSp.getSelectedRow();
        if (rowSelected != -1) {
            String ma = (String) tblDongSp.getValueAt(rowSelected, 1);
            String ten = (String) tblDongSp.getValueAt(rowSelected, 2);

            txtMaDongSP.setText(ma);
            txtTenDongSp.setText(ten);
        }
    }//GEN-LAST:event_tblDongSpMouseClicked

    private QLDongSP validateThongTin() {
        boolean isValid = true;

        String ma = txtMaDongSP.getText();
        if (ma.isBlank()) {
            JOptionPane.showMessageDialog(this, "Khong duoc bo trong ma");
            isValid = false;
        }

        String ten = txtTenDongSp.getText();
        if (ten.isBlank()) {
            JOptionPane.showMessageDialog(this, "Khong duoc bo trong ten");
            isValid = false;
        }

        QLDongSP sp = null;
        if (isValid == true) {
            sp = new QLDongSP(ma, ten);
        }
        return sp;
    }

    private void reset() {
        txtMaDongSP.setText("");
        txtTenDongSp.setText("");
    }

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        QLDongSP qldsp = validateThongTin();
        if (qldsp == null) {
            JOptionPane.showMessageDialog(this, "Không có data");
            return;
        }

        if (imangeDongSp.save(qldsp) != null) {
            JOptionPane.showMessageDialog(this, "Thành công");
            reset();
        } else {
            JOptionPane.showMessageDialog(this, "Thất bại");
            return;
        }
        loadTable();
    }//GEN-LAST:event_btnThemActionPerformed

    private String getSelectedDongSpById() {
        int rowSelected = tblDongSp.getSelectedRow();
        return (String) tblDongSp.getValueAt(rowSelected, 0);
    }

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        QLDongSP updateDongSp = validateThongTin();
        if (updateDongSp == null) {
            JOptionPane.showMessageDialog(this, "Không có data");
            JOptionPane.showMessageDialog(this, "Thất bại");
            return;
        }

        String id = getSelectedDongSpById();
        updateDongSp.setIdDongSP(id);

        if (imangeDongSp.updateDongSpById(updateDongSp) != null) {
            JOptionPane.showMessageDialog(this, "Thành công");
            reset();
        } else {
            JOptionPane.showMessageDialog(this, "Thất bại");
        }
        loadTable();
    }//GEN-LAST:event_btnSuaActionPerformed

