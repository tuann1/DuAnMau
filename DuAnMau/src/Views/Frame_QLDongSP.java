    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblDongSp.getModel();
        model.setRowCount(0);
        List<QLDongSP> lstDongSp = imangeDongSp.getAll();
        for (QLDongSP dongSP : lstDongSp) {
            model.addRow(new Object[]{
                dongSP.getIdDongSP(), dongSP.getMaDongSp(), dongSP.getTenDongSp()
            });
        }
    }

    private void loadTable2(List<QLDongSP> list) {
        DefaultTableModel model = (DefaultTableModel) tblDongSp.getModel();
        model.setRowCount(0);
        for (QLDongSP dongSP : list) {
            model.addRow(new Object[]{
                dongSP.getIdDongSP(), dongSP.getMaDongSp(), dongSP.getTenDongSp()
            });
        }
    }

