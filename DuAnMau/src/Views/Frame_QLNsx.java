    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblNsx.getModel();
        model.setRowCount(0);
        List<QLNsx> lstNsx = manageNsxService.getAll();
        for (QLNsx nsx : lstNsx) {
            model.addRow(new Object[]{
                nsx.getId(), nsx.getMaNSx(), nsx.getTenNSx()
            });
        }
    }

    private void loadTable2(List<QLNsx> list) {
        DefaultTableModel model = (DefaultTableModel) tblNsx.getModel();
        model.setRowCount(0);
        for (QLNsx nsx : list) {
            model.addRow(new Object[]{
                nsx.getId(), nsx.getMaNSx(), nsx.getTenNSx()
            });
        }
    }

    private QLNsx validateThongTin() {
        QLNsx nsx = null;
        boolean isValid = true;

        String ma = txtMaNsx.getText();
        try {
            if (ma.isBlank()) {
                JOptionPane.showMessageDialog(this, "Khong duoc bo trong");
                isValid = false;
            }
        } catch (HeadlessException e) {
            isValid = false;
        }

        String ten = txtTenNsx.getText();
        try {
            if (ten.isBlank()) {
                JOptionPane.showMessageDialog(this, "Khong duoc bo trong");
                isValid = false;
            }
        } catch (HeadlessException e) {
            isValid = false;
        }

        if (isValid == true) {
            nsx = new QLNsx(ma, ten);
        }

        return nsx;
    }

