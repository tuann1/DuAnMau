    private void tblNsxMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNsxMouseClicked
        // TODO add your handling code here:
        int rowSelected = tblNsx.getSelectedRow();
        if (rowSelected != -1) {
            String ma = (String) tblNsx.getValueAt(rowSelected, 1);
            String ten = (String) tblNsx.getValueAt(rowSelected, 2);

            txtMaNsx.setText(ma);
            txtTenNsx.setText(ten);
        }
    }//GEN-LAST:event_tblNsxMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int rowSelected = tblNsx.getSelectedRow();
        if (rowSelected != -1) {
            String id = (String) tblNsx.getValueAt(rowSelected, 0);
            manageNsxService.delete(id);
            loadTable();
            JOptionPane.showMessageDialog(this, "Thành công!");
            reset();
        } else {
            JOptionPane.showMessageDialog(this, "Chưa chọn dòng để xóa.");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void reset() {
        txtMaNsx.setText("");
        txtTenNsx.setText("");
    }

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        QLNsx a = validateThongTin();
        if (a == null) {
            JOptionPane.showMessageDialog(this, "Thất bại");
            return;
        }
        if (manageNsxService.insert_Sql(a) != null) {
            JOptionPane.showMessageDialog(this, "Thành công");
            reset();
        } else {
            JOptionPane.showMessageDialog(this, "Thất bại");
        }
        loadTable();
    }//GEN-LAST:event_btnThemActionPerformed

    private String getSelectId() {
        int rowSelected = tblNsx.getSelectedRow();
        return (String) tblNsx.getValueAt(rowSelected, 0);
    }

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        QLNsx updateNsx = validateThongTin();
        if (updateNsx == null) {
            JOptionPane.showMessageDialog(this, "Không có data");
            return;
        }
        String id = getSelectId();
        updateNsx.setId(id);

        if (manageNsxService.updateById(updateNsx) != null) {
            JOptionPane.showMessageDialog(this, "Thành công");

            reset();
        } else {
            JOptionPane.showMessageDialog(this, "Thất bại");
        }
        loadTable();
    }//GEN-LAST:event_btnSuaActionPerformed

